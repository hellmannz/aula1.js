// Operadores de comparação
console.log("1" == 1)
console.log(true == 1)
console.log(false == 0)

//Operador de igualdade estrita
// (compara o valor e tipo)
console.log("1" === 1)
console.log(true === 1)
console.log(false === 0)

// Verificar tipo da variável
console.log(typeof ("1")) // -> texto
console.log(typeof(1)) // -> Number
console.log(typeof(true)) // -> boolean
